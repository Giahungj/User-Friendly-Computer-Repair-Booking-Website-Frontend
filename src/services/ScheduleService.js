import axios from "../setUp/axios";
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
const getTechnicianWorkScheduleListForStoreManager = async (storeManagerId) => {
    try {
        const response = await axios.get(`/api/cua-hang-truong/${storeManagerId}/ky-thuat-vien/lich-lam-viec`);
        return response;
    } catch (error) {
        console.error("Error fetching doctor schedules:", error.response ? error.response.data : error.message);
        return { EC: -1, EM: "Error fetching doctor schedules", DT: null };
    }
};
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
const createWorkSchedules = async (payload) => {
    try {
        const response = await axios.post("/api/cua-hang-truong/lich-lam-viec/tao-moi", payload);
        return response;
    } catch (error) {
        console.error("FetchFalse:", error);
        return null;
    }
};
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
const updateWorkSchedules = async (scheduleData) => {
    try {
        const response = await axios.put(`/api/update-schedule`, scheduleData);
        return response;
    } catch (error) {
        console.error("FetchFalse:", error);
        return null;
    }
};
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
export {
    getTechnicianWorkScheduleListForStoreManager,
    createWorkSchedules,
    updateWorkSchedules
};

