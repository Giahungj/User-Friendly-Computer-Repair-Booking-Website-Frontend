import axios from "../setUp/axios";
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------------------
const getRatingByTechnician = async (technicianId) => {
    try {
        const response = await axios.get(`/api/ky-thuat-vien/${technicianId}/thong-tin/danh-gia`);
        return response;
    } catch (error) {
        console.error(`Fetch rating failed (technician ${technicianId}):`, error.message);
        return { EC: -1, EM: error.message || "Lỗi không xác định", DT: [] };
    }
};
